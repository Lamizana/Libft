#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int			i;
	long int	nb;
	int			signe;

	i = 0;
	nb = 0;
	signe = 0;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-')
	{
		signe++;
		i++;
	}
	else if (nptr[i] == '+')
		i++;
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		nb = (nb * 10) + (nptr[i] - '0');
		i++;
	}
	if (signe == 1)
		nb = nb * -1;
	return (nb);
}
